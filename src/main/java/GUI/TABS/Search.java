	/**
	 * Nastaví hodnoty vyhledávacího formuláře do struktury.
	 */
	public void setValStruct()
	{
		// uložení hodnot formuláře do struktury
        ta.I_id = (id.getText().isEmpty() ? -1 : Integer.parseInt(id.getText()));	
        ta.I_stanice = ((((MyComboBox)station.getSelectedItem()) == null) ? -1 : ((MyComboBox)station.getSelectedItem()).getValue());
        ta.I_vek = (age.getText().isEmpty() ? -1 : Integer.parseInt(age.getText()));
        ta.I_zpozdeni = (delay.getText().isEmpty() ? -1 : Integer.parseInt(delay.getText()));
        ta.I_udalost = event.getSelectedItem().toString();
        ta.I_zraneni = injury.getSelectedItem().toString();
		ta.O_id = ((((MyComboBox)area.getSelectedItem()) == null) ? -1 : ((MyComboBox)area.getSelectedItem()).getValue());
		ta.L_id = ((((MyComboBox)line.getSelectedItem()) == null) ? -1 : ((MyComboBox)line.getSelectedItem()).getValue());
		ta.I_pricina = reason.getSelectedItem().toString();
        ta.I_pohlavi = sex.getSelectedItem().toString();
        ta.I_dateFrom = (String) datePicker1.getJFormattedTextField().getText();
        ta.I_dateTo = (String) datePicker2.getJFormattedTextField().getText();
	}
	
	/**
	 * Vyhledá incidenty v DB dle zadaných údajů ve formuláři.
	 */
	public void searchIncidents()
	{
		// získání hodnot formuláře
		setValStruct();
				
		// aplikování filtru na tabulku a naplnění výsledky
        jTable1.setModel(GUI.MYOBJECTS.MyTableModel.buildTableModel(DB.Queries.Incident.getIncidents(ta)));
		
		// aplikování filtru na mapu
        mapSearchViewer1.setIncident(ta);
		
        try {
            // překreslení mapy
             mapSearchViewer1.loadFilteredObjectFromDb();
        } catch (Exception ex) {
            Logger.getLogger(Search.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        mapSearchViewer1.repaint();
	}
	   
