	/**
	 * Zobrazí modální okno pro úpravu incidentu po kliknutí na řádek tabulky.
	 * @param evt předaná událost
	 */
    private void showIncident(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_showIncident
		// číslo řádku daného incidentu
		int row = jTable1.rowAtPoint(evt.getPoint());
		
		// vytvoření okna s incidentem
		Main.dIncident = new Incident(Main.TopLevelWindow, true);
		
		// získání ID incidentu v DB
		int incidentID = Integer.parseInt(jTable1.getValueAt(row, 0).toString());

		// inicializace otevřeného okna pro úpravu záznamu incidentu v DB
		Main.dIncident.initForUpdate(incidentID, this);
		Main.dIncident.setVisible(true);
		
    }//GEN-LAST:event_showIncident
	
